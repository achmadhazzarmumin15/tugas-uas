<font color="blue"><h5>Sandiaga Uno yang mengundurkan diri dari jabatannya sebagai Wakil Gubernur DKI Jakarta pada hari ini.

Ketua Fraksi NasDem DPRD DKI Bestari Barus mengatakan, dalam rapat tersebut, nantinya akan ada permintaan persetujuan kepada dewan terkait pengajuan permohonan Sandiaga Uno untuk berhenti.

Dia berharap Sandiaga Uno selaku pihak yang ingin mengundurkan diri dapat hadir dalam rapat tersebut.

Menurut dia, Sandiaga sebenarnya wajib untuk hadir dalam rapat tersebut. Namun begitu, menurut Bestari, tidak ada sanksi yang diberikan jika cawapres pendamping Prabowo Subianto pada Pilpres 2019 itu berhalangan hadir.

"Ya enggak (ada sanksi) juga, sanksi apa orang sudah mundur. (Paripurna) tetap berlangsung,” kata Bestari seperti dilansir dari JawaPos.com.

Sebelumnya, Gubernur DKI Jakarta Anies Baswedan memastikan Sandiaga akan hadir untuk membacakan sendiri surat pernyataan berhentinya sesuai permintaan Bamus DPRD DKI Jakarta.

"Keliatannya, sih, datang,” ujar Anies di Balai Kota DKI Jakarta, Jumat, 24 Agustus 2018.

Hal itu disampaikannya usai bertemu Sandiaga dalam perayaan HUT Partai Amanat Nasional (PAN), Kamis, 23 Agustus 2018. "Kita ngobrol soal hari Senin, mudah-mudahan, lagi dicocokin (jadwalnya),” kata dia.
</h5>
