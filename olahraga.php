<font color="blue"><h5>Satu medali emas kembali diraih atlet Indonesia dalam ajang Karate Asian Games 2018 yang berlangsung di Plenary Hall  Jakarta Convention Center, Jakarta. Penyumbang medali emas itu rupanya seorang anggota TNI bernama Serda Rifki Ardiansyah. 

Rifki dinyatakan sebagai pemenang kejuaraan karate Asian Games setelah mengalahkan atlet karate asal Iran, Amir Mahdi Zadeh, dalam kelas kumite 60 kg dengan torehan skor 9-7 pada laga final.Rifki sendiri menetap di Surabaya dan sehari-harinya bertugas sebagai Babanmin 2 Pokbanmin Jasdam V/Brawijaya. Putra dari pasangan Surya dan Dwi ini menempuh pendidikan Bintara tahun 2016 lalu. 

Panglima Kodam (Pangdam) V/Brawijaya, Mayjen TNI Arif Rahman mengaku bangga prajuritnya bisa menorehkan prestasi gemilang. Rifki membawa harum nama Kodam V/Brawijaya dalam menjunjung tinggi nama baik Indonesia dalam dunia olahraga.

"Nanti kita rencanakan ada prosesi penyambutan dan pemberian penghargaan terhadap Serda Rifki," ujar Pangdam.

Keberhasilan yang diraih oleh Rifki tersebut dijadikan suatu motivasi untuk terus meningkatkan latihan. "Mudah-mudahan, dia (Rifki) bisa menyalurkan bakat dan kemampuan ke rekan-rekan lainnya," pintanya.

Sebelum meraih emas di ajang Asian Games, Serda Rifki Ardiansyah pernah meraih medali perunggu di SEA Games 2017 dan Emas dari kelas Kumite 55 Kg dalam Kejurnas Karate Piala Panglima TNI tahun 2017 lalu.</h5>
